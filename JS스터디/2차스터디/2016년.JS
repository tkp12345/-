function solution(a, b) {
    var answer = '';
    let 요일  =  [ 'SUN','MON','TUE','WED','THU','FRI','SAT' ];
    //2021 년기준
    let 월별요일  =  [ 31,28,31,30,31,30,31,31,30,31,30,31];
    let 일 = [] 
    let 원하는일 = 0; 

    // 달력만들기 (1월1일 -> 금요일) (i+5)%7 
    for (let i=0; i<= 365; i++){
        일[i] =  요일[(i+5)&7];
    }
    
    // 원하는날짜 일수 전달 만큼 원하는일에 할당
    for (let i=0; i<a-1; i++){
        원하는일 += 월별요일[i];
    }

    //전달일수 에  선택월일수 만큼 다한다 
    원하는일 += b-1;

    answer = 일[원하는일];
    console.log(answer);
    return answer;
}

